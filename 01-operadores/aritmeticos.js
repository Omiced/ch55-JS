//Operador de incremento ++, incrementa el valor actual de la varible en 1
let counter = 0;
//este incremento primero evalua y luego suma 1
console.log(counter++);
console.log(counter);
//este incremento primero suma y luego evalua
console.log(++counter);
//Operador de decremento --, disminuye el valor actual de la variable en 1
//primero evalua luego resta
console.log(counter--);
console.log(counter);
//primero resta y luego evalua
console.log(--counter);
//operacion y reasignacion
counter += 10;
console.log(counter);
counter -= 5;
console.log(counter);
counter *= 10;
console.log(counter);
counter /= 2;
console.log(counter);
counter %= 10;
console.log(counter);
//toma el valor que tiene la variable actualmente como base y lo eleva al valor nuevo
counter **= 3;
console.log(counter);
